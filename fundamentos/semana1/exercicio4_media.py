#Exercício 4 — Média
#	peça 3 números 
#	mostre: Média: X

# Exercício Concluído
numero_1 = float(input("Digite o número 1: "))
numero_2 = float(input("Digite o número 2: "))
numero_3 = float(input("Digite o número 3: "))

media = ((numero_1 + numero_2 + numero_3) / 3)

print(f"A média dos números informados é: {media}")
print(f"Média: {(numero_1 + numero_2 + numero_3) / 3}")


